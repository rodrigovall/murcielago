  let indice = 0;

  function mostrarSeccion(indice) {
    const seccion = secciones[indice];
    document.getElementById("titulo").textContent = seccion.titulo;
    document.getElementById("descripcion").innerHTML = seccion.descripcion;
    const img = document.getElementById("imagen");
    img.src = seccion.imagen;
    img.alt = seccion.alt;
  }

  document.getElementById("btnSiguiente").addEventListener("click", () => {
    indice++;
    if (indice >= secciones.length) {
      indice = 0; // vuelve al inicio cuando termina
    }
    mostrarSeccion(indice);
  });

  // Mostrar la primera sección al cargar la página
  mostrarSeccion(indice);


  document.getElementById("btnAnterior").addEventListener("click", () => {
    indice--;
    if (indice < 0) {
      indice = secciones.length - 1; // vuelve al final si llega al inicio
    }
    mostrarSeccion(indice);
  });
  